#define F_CPU 1000000UL
#include <avr/io.h>
#include <util/delay.h>

#define LED PB0



int main(void)
{

	//DDRB |= (1<<LED); // set LED pin as output
	DDRB = 0b00000010;

	while(1)
	{
		PORTB |= (1<<PB1); // toggle the led state
		_delay_ms(1000);
		PORTB &= ~(1<<PB1);
		_delay_ms(1000);
	}

	return 0;
}

